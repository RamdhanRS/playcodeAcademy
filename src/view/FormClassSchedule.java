    private void loadData() {
        btnHapus.setVisible(false);
        btnBatal.setVisible(false);
        List<ClassScheduleModel> list = classScheduleService.getData();
        classScheduleTableModel.setData(list);

        tblClassSchedule.getColumnModel().getColumn(0).setMinWidth(0);
        tblClassSchedule.getColumnModel().getColumn(0).setMaxWidth(0);
        tblClassSchedule.getColumnModel().getColumn(0).setWidth(0);
    }

    private void createData() {
        if (validasiInput() == true) {
            int classId = Integer.parseInt(txtClassesId.getText());
            String dayOfWeek = cbxDayOfWeek.getSelectedItem().toString();
            java.sql.Time startTime = new java.sql.Time(((Date) txtStartTime.getValue()).getTime());
            java.sql.Time endTime = new java.sql.Time(((Date) txtEndTime.getValue()).getTime());

            // Mengambil hasil relasi nya
            ClassesModel getClassesModel = classesService.getById(classId);

            ClassScheduleModel classScheduleModel = new ClassScheduleModel();
            classScheduleModel.setClasses(getClassesModel);
            classScheduleModel.setDayOfWeek(dayOfWeek);
            classScheduleModel.setStartTime(startTime.toLocalTime());
            classScheduleModel.setEndTime(endTime.toLocalTime());

            classScheduleService.addClassSchedule(classScheduleModel);
            classScheduleTableModel.addClassSchedule(classScheduleModel);

            loadData();
            resetForm();
            showPanel();
            btnSimpan.setText("Tambah");
        }
    }

    private void updateData() {
        int row = tblClassSchedule.getSelectedRow();
        if (row != -1) {
            if (validasiInput() == true) {
                int classId = Integer.parseInt(txtClassesId.getText());
                String dayOfWeek = cbxDayOfWeek.getSelectedItem().toString();
                java.sql.Time startTime = new java.sql.Time(((Date) txtStartTime.getValue()).getTime());
                java.sql.Time endTime = new java.sql.Time(((Date) txtEndTime.getValue()).getTime());

                // Mengambil hasil relasi nya
                ClassesModel getClassesModel = classesService.getById(classId);

                ClassScheduleModel classScheduleModel = new ClassScheduleModel();
                classScheduleModel.setId((int) tblClassSchedule.getModel().getValueAt(row, 0));
                classScheduleModel.setClasses(getClassesModel);
                classScheduleModel.setDayOfWeek(dayOfWeek);
                classScheduleModel.setStartTime(startTime.toLocalTime());
                classScheduleModel.setEndTime(endTime.toLocalTime());

                classScheduleService.editClassSchedule(classScheduleModel);
                classScheduleTableModel.editClassSchedule(row, classScheduleModel);
                loadData();
                resetForm();
                showPanel();
            }
        }
    }

    private void deleteData() {
        int index = tblClassSchedule.getSelectedRow();
        if (index != -1) {
            int classId = (int) tblClassSchedule.getModel().getValueAt(index, 0);
            ClassScheduleModel getClass = classScheduleService.getById(classId);

            ClassScheduleModel classScheduleModel = getClass;

            if (JOptionPane.showConfirmDialog(null, "Yakin data akan dihapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                classScheduleService.deleteClassSchedule(classScheduleModel);
                classScheduleTableModel.deleteClassSchedule(index);
                loadData();
                resetForm();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih dahulu record yang akan dihapus");
        }
    }

    private boolean validasiInput() {
        boolean valid = false;
        if (txtClasses.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nama Kelas tidak boleh kosong");
        } else {
            valid = true;
        }

        return valid;
    }

    private void showPanel() {
        mainPanel.removeAll();
        mainPanel.add(new FormClassSchedule());
        mainPanel.repaint();
        mainPanel.revalidate();
    }

    private void resetForm() {
        btnTambah.requestFocus();
        btnTambah.setText("Tambah");
        txtClasses.setText("");
        txtClassesId.setText("");
        txtStartTime.setValue(new Date());
        txtEndTime.setValue(new Date());
        cbxDayOfWeek.setSelectedIndex(0);
    }

