    private void dataTabel() {
        dataClassSchedule.setVisible(false);
        addClassSchedule.setVisible(true);

        int row = tblClassSchedule.getSelectedRow();
        jLabel2.setText("Perbarui Data Kelas");

        int classScheduleId = (int) tblClassSchedule.getModel().getValueAt(row, 0);
        ClassScheduleModel classScheduleModel = classScheduleService.getById(classScheduleId);

        txtClasses.setText(classScheduleModel.getClasses().getClassName());
        txtClassesId.setText(classScheduleModel.getClasses().getId().toString());
        cbxDayOfWeek.setSelectedItem(classScheduleModel.getDayOfWeek());
        txtStartTime.setValue(Date.from(classScheduleModel.getStartTime().atDate(LocalDate.now()).atZone(ZoneId.systemDefault()).toInstant()));
        txtEndTime.setValue(Date.from(classScheduleModel.getEndTime().atDate(LocalDate.now()).atZone(ZoneId.systemDefault()).toInstant()));
        active();
        btnSimpan.setText("Perbarui");
        btnBatalTambah.setVisible(true);
    }

