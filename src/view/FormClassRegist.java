    private void loadData() {
        btnHapus.setVisible(false);
        btnBatal.setVisible(false);
        List<ClassRegistModel> list = classRegistService.getData();
        classRegistTableModel.setData(list);

        tblClassRegist.getColumnModel().getColumn(0).setMinWidth(0);
        tblClassRegist.getColumnModel().getColumn(0).setMaxWidth(0);
        tblClassRegist.getColumnModel().getColumn(0).setWidth(0);
    }

    private void loadDataDetail(int id) {
        System.out.println("Masuk sini ndak? : " + id);
        btnHapus.setVisible(false);
        btnBatal.setVisible(false);
        tblDetail.setModel(userTableModel);

        List<UserModel> list = classRegistService.getDataStudentById(id);
        userTableModel.setData(list);

        lbl_total_student.setText(Integer.toString(userTableModel.getRowCount()));
        tblDetail.getColumnModel().getColumn(0).setMinWidth(0);
        tblDetail.getColumnModel().getColumn(0).setMaxWidth(0);
        tblDetail.getColumnModel().getColumn(0).setWidth(0);
    }

    private void createData() {
        if (validasiInput() == true) {
            int classId = Integer.parseInt(txtClassesId.getText());
            int studentId = Integer.parseInt(txtStudentId.getText());

            // Mengambil hasil relasi nya
            ClassesModel getClassesModel = classesService.getById(classId);
            UserModel getStudentModel = userService.getById(studentId);

            ClassRegistModel classRegistModel = new ClassRegistModel();
            classRegistModel.setClasses(getClassesModel);
            classRegistModel.setStudent(getStudentModel);

            classRegistService.addClassRegist(classRegistModel);
            classRegistTableModel.addClassRegist(classRegistModel);

            loadData();
            resetForm();
            showPanel();
            btnSimpan.setText("Tambah");
        }
    }

    private void deleteData() {
        int index = tblClassRegist.getSelectedRow();
        if (index != -1) {
            int classId = (int) tblClassRegist.getModel().getValueAt(index, 0);
            ClassRegistModel getClass = classRegistService.getById(classId);

            ClassRegistModel classRegistModel = getClass;

            if (JOptionPane.showConfirmDialog(null, "Yakin data akan dihapus?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                classRegistService.deleteClassRegist(classRegistModel);
                classRegistTableModel.deleteClassRegist(index);
                loadData();
                resetForm();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Pilih dahulu record yang akan dihapus");
        }
    }

    private boolean validasiInput() {
        boolean valid = false;
        if (txtClasses.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nama Kelas tidak boleh kosong");
        } else {
            valid = true;
        }

        return valid;
    }

    private void showPanel() {
        mainPanel.removeAll();
        mainPanel.add(new FormClassRegist());
        mainPanel.repaint();
        mainPanel.revalidate();
    }

    private void resetForm() {
        btnTambah.requestFocus();
        btnTambah.setText("Tambah");
        txtClasses.setText("");
        txtClassesId.setText("");
        txtStudent.setText("");
        txtStudentId.setText("");
    }

