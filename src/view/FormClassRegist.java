    private void dataTabel() {
        dataClassRegist.setVisible(false);
        addClassRegist.setVisible(false);
        detailClassRegist.setVisible(true);

        int row = tblClassRegist.getSelectedRow();
        jLabel4.setText("Detail Data Daftar Kelas");

        int classRegistId = (int) tblClassRegist.getModel().getValueAt(row, 0);
        ClassRegistModel classRegistModel = classRegistService.getById(classRegistId);

        loadDataDetail(classRegistId);
        lbl_class.setText(classRegistModel.getClasses().getClassName());
        lbl_courses.setText(classRegistModel.getCourses().getCourseName());
        lbl_coach.setText(classRegistModel.getCoach().getNama());

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd MMMM yyyy", new Locale("id", "ID"));
        String result = classRegistModel.getClasses().getStartDate().format(formatter) + " s/d " + classRegistModel.getClasses().getEndDate().format(formatter);
        lbl_date_range.setText(result);
    }

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        mainPanel.removeAll();
        mainPanel.repaint();
        mainPanel.revalidate();

        mainPanel.add(addClassRegist);
        mainPanel.repaint();
        mainPanel.revalidate();

        btnSimpan.setText("Simpan");
        if (btnTambah.getText().equals("Detail")) {
            mainPanel.add(detailClassRegist);
            mainPanel.repaint();
            mainPanel.revalidate();

            btnSimpan.setVisible(false);
            dataTabel();
        }
    }//GEN-LAST:event_btnTambahActionPerformed

